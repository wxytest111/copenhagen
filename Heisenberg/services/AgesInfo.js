class AgesInfo{
    constructor(){

    }
    get Ages(){
        return this.ages;
    }
    set Ages(ages){
        this.ages = ages;
    } 
    get Count(){
        return this.count;
    }
    set Count(count){
        this.count = count;
    }
    get Gender(){
        return this.gender;
    }
    set Gender(gender){
        this.gender = gender;
    }
    
    
}
module.exports = AgesInfo;

