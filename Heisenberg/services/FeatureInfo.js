class FeatureInfo{
    constructor(){

    }
    get Gender(){
        return this.gender;
    }
    set Gender(gender){
        this.gender = gender;
    }
    get Age(){
        return this.age;
    }
    set Age(age){
        this.age = age;
    }
    get Emotion(){
        return this.emotion;
    }
    set Emotion(emotion){
        this.emotion = emotion;
    }
    
}
module.exports = FeatureInfo;

